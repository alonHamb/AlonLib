package org.firstinspires.ftc.teamcode.opmodes.teleop;

import com.acmerobotics.dashboard.FtcDashboard;
import com.acmerobotics.dashboard.config.Config;
import com.acmerobotics.dashboard.telemetry.MultipleTelemetry;
import com.bylazar.telemetry.PanelsTelemetry;
import com.qualcomm.robotcore.eventloop.opmode.OpMode;
import com.qualcomm.robotcore.eventloop.opmode.TeleOp;
import com.seattlesolvers.solverslib.hardware.motors.Motor;

import org.firstinspires.ftc.teamcode.alonlib.hardware.motors.HaMotor;
import org.firstinspires.ftc.teamcode.alonlib.math.PIDFGains;

/*
 * This file contains an example of an iterative (Non-Linear) "OpMode".
 * An OpMode is a 'program' that runs in either the autonomous or the teleop period of an FTC match.
 * The names of OpModes appear on the menu of the FTC Driver Station.
 * When a selection is made from the menu, the corresponding OpMode
 * class is instantiated on the Robot Controller and executed.
 *
 * This particular OpMode just executes a basic Tank Drive Teleop for a two wheeled robot
 * It includes all the skeletal structure that all iterative OpModes contain.
 *
 * Use Android Studio to Copy this Class, and Paste it into your team's code folder with a new name.
 * Remove or comment out the @Disabled line to add this OpMode to the Driver Station OpMode list
 */

@TeleOp(name = "Java Test", group = "Teleop")
@Config
public class JavaTestOpMode extends OpMode {

    public static HaMotor motor;
    public static Double kP = 1.0;

    public static Double kI = 0.0;

    public static Double kD = 0.0;

    public static Double kF = 0.0;

    public static Double setPoint = 0.0;

    @Override
    public void init() {
        telemetry = new MultipleTelemetry(telemetry, FtcDashboard.getInstance().getTelemetry(), PanelsTelemetry.INSTANCE.getFtcTelemetry());
        motor = new HaMotor(hardwareMap, "m3", Motor.GoBILDA.RPM_312);
        motor.setRunMode(Motor.RunMode.PositionControl);


    }


    @Override
    public void loop() {
        motor.setPidfGains(new PIDFGains(kP, kI, kD, kF));
        motor.setSetPoint(setPoint);
        telemetry.addData("motor position", motor.getPosition().getDegrees());
        telemetry.addData("motor error", motor.getError());
    }


}
